
package redbeechallenges;




public class SumArrayNumbers {

    public static void main(String[] args) {      
          
        Integer list [] = { 1, 2,3};

        Integer sum = 0;
	
        for (int i = 0; i < list.length; i++) {
	sum = sum + list[i];
	}
        
        System.out.println(sum);
        
    }
   
 
}   
     
    


